// Initialize i18n
const initLanguage = localStorage.getItem('language') || 'en';

// Force English if no language is set
if (!localStorage.getItem('language')) {
  localStorage.setItem('language', 'en');
}

i18n
  .use(initReactI18next)
  .init({
    resources,
    lng: initLanguage,
    fallbackLng: 'en',
    debug: true, // Enable debug mode to see what's happening
    interpolation: {
      escapeValue: false,
    },
    react: {
      useSuspense: false,
    },
  });

// Ensure the language is set correctly after initialization
i18n.changeLanguage(initLanguage);

export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState(
    localStorage.getItem('language') || 'en'
  );
  const [forceUpdate, setForceUpdate] = useState(0);

  // Function to update document direction based on language
  const updateDocumentDirection = (language) => {
    const isRTL = RTL_LANGUAGES.includes(language);
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = language;
    
    // Add/remove RTL class to body
    if (isRTL) {
      document.body.classList.add('rtl');
    } else {
      document.body.classList.remove('rtl');
    }
  };

  // Initialize language on component mount
  useEffect(() => {
    const savedLanguage = localStorage.getItem('language') || 'en';
    
    // Ensure i18n and React state are in sync
    if (i18n.language !== savedLanguage) {
      i18n.changeLanguage(savedLanguage);
    }
    
    setCurrentLanguage(savedLanguage);
    updateDocumentDirection(savedLanguage);
  }, []);

  // Sync i18n when currentLanguage changes
  useEffect(() => {
    if (i18n.language !== currentLanguage) {
      i18n.changeLanguage(currentLanguage);
    }
  }, [currentLanguage]);

  const changeLanguage = (language) => {
    setCurrentLanguage(language);
    i18n.changeLanguage(language);
    localStorage.setItem('language', language);
    updateDocumentDirection(language);
    setForceUpdate(prev => prev + 1); // Force re-render
  };

  const value = {
    currentLanguage,
    changeLanguage,
    t: (key, options) => {
      // Ensure i18n is using the correct language
      if (i18n.language !== currentLanguage) {
        i18n.changeLanguage(currentLanguage);
      }
      
      return i18n.t(key, options);
    },
    isRTL: RTL_LANGUAGES.includes(currentLanguage),
    forceUpdate, // Include forceUpdate to trigger re-renders
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};
